let test = new LocationData(tutorial);
let map = new Minimap('minimap', test);
let render = new Renderer('game', test.worldMap);
let joystickA = new Joystick('joystickA', 64);
let joystickB = new Joystick('joystickB', 64);

let client = document.getElementById("gameContainer");
let controls = document.getElementById("controller");

test.attach();
run();

function run() {
    if (client.clientWidth > 600)
    {
        controls.style.display = "none";
    }
    else
    {
        controls.style.display = "grid";
    }

    test.update();
    test.moveTo(joystickA);
    test.stickRotate(joystickB);
    let endpoints = RayCaster.castArea(test.coordinateX, test.coordinateY, test.coordinateAngle, test.fov, client.clientWidth, test.worldMap);
    map.clearScreen();
    map.drawMap(test.worldMap);
    map.draw(endpoints);
    render.draw(endpoints);
    let thePath = aStar.explore({x:Math.floor(test.coordinateX),y:Math.floor(test.worldHeight - test.coordinateY)}, {x:30, y:22}, tutorial);
    map.drawPath(thePath);
    requestAnimationFrame(run);
}
